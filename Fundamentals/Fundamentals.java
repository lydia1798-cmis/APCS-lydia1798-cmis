
public class Fundamentals

{

    public static void main (String[] args){
        dataTypes();
    }

    public static void dataTypes(){
        System.out.println( "dataTypes:\n" );

        byte byteExample = 58;
        System.out.println( "byte: " + byteExample + "\n");

        short shortExample = -23678;
        System.out.println( "short: " + shortExample + "\n");

        int intExample = 45758678; 
        System.out.println( "int: " + intExample + "\n");

        long longExample = -2^50;
        System.out.println("long: " + longExample + "\n");

        float floatExample = 56.6f;
        System.out.println("float: " + floatExample + "\n");

        double doubleExample = 78889.87;
        System.out.println("double: " + doubleExample + "\n");

        boolean booleanExample = true;
        System.out.println("boolean: " + booleanExample + "\n");

        char charExample = 'a';
        System.out.println("char: " + charExample + "\n");

        
    }
}
